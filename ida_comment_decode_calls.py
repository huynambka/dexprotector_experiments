# IDAPython: scan all calls to sub_401B0/decode(), decode the string blob,
# and add comments at the BL instruction/call site, pseudocode, and input blob.
#
# Usage in IDA: File -> Script file... -> ida_comment_decode_calls.py
# Requires IDA database loaded at raw unpacked image base 0.

from __future__ import print_function
import struct

import ida_bytes
import ida_idaapi
import ida_kernwin
import idc
import idautils

try:
    import ida_hexrays
except Exception:
    ida_hexrays = None

# ---- config ----
DECODE_EA_FALLBACK = 0x401B0
MAX_BACK_INSNS = 18
COMMENT_BLOB = True
COMMENT_CALL = True              # comment at BL decode call site
COMMENT_SETUP_SOURCE = True      # also comment at predecessor/setup branch for shared decode blocks
COMMENT_PSEUDOCODE = True        # add Hex-Rays pseudocode user comments too
PSEUDOCODE_ITP_NAME = "ITP_EMPTY" # if not visible, try "ITP_EMPTY"

# qword_8CB40 table generated by init sub_37FF8; verified with known strings.
TABLE = bytes.fromhex(
    "61d5ccc1cc0659b29b30e842f6fab3bd15edef6db465bf039775912d6e499aa"
    "20942d5d627bcafb8382f0055894fc3797529cb2ce6ec025423c8c33f6fd8b"
    "88065f72dca2a77a28a86a432549aab97cfa325cea8c96bbe3c0430df004b"
    "234a70dde895c6bd53d420909ae98a"
)

# ---- tiny logging ----
def msg(s):
    ida_kernwin.msg(str(s) + "\n")


def u32(b, off):
    return struct.unpack_from("<I", b, off)[0]


def p32(x):
    return struct.pack("<I", x & 0xffffffff)


def ror32(x, n):
    x &= 0xffffffff
    return ((x >> n) | ((x << (32 - n)) & 0xffffffff)) & 0xffffffff


def decode_401B0(blob_ea, length):
    blob = ida_bytes.get_bytes(blob_ea, 8 + length)
    if blob is None or len(blob) < 8 + length:
        raise RuntimeError("cannot read blob 0x%x len=%d" % (blob_ea, length))

    w9 = u32(blob, 0)  # state_a / a1[0]
    w8 = u32(blob, 4)  # state_b / a1[1]
    out = bytearray()
    ks = b"\x00" * 8

    for i in range(length):
        if (i & 7) == 0:
            for j in range(0, 0x6c, 4):
                t = u32(TABLE, j)
                w8 = (t ^ ((ror32(w8, 8) + w9) & 0xffffffff)) & 0xffffffff
                w9 = (w8 ^ ror32(w9, 29)) & 0xffffffff
            ks = p32(w9) + p32(w8)  # sub_401B0 writes W9 bytes first, then W8
        out.append(blob[8 + i] ^ ks[i & 7])
    return bytes(out)


def pretty_bytes(b):
    raw = b.split(b"\x00", 1)[0]
    try:
        s = raw.decode("utf-8")
    except Exception:
        # safe escaped representation for binary-ish output
        s = ''.join(chr(x) if 0x20 <= x <= 0x7e else "\\x%02x" % x for x in raw)
    return s.replace("\\", "\\\\").replace('"', '\\"')


HEXRAYS_READY = False
CFUNC_CACHE = {}


def init_hexrays_once():
    global HEXRAYS_READY
    if not COMMENT_PSEUDOCODE:
        return False
    if HEXRAYS_READY:
        return True
    if ida_hexrays is None:
        return False
    try:
        HEXRAYS_READY = bool(ida_hexrays.init_hexrays_plugin())
    except Exception:
        HEXRAYS_READY = False
    return HEXRAYS_READY


def get_cfunc_for_ea(ea):
    if not init_hexrays_once():
        return None
    fstart = idc.get_func_attr(ea, idc.FUNCATTR_START)
    if fstart == ida_idaapi.BADADDR:
        return None
    if fstart not in CFUNC_CACHE:
        try:
            CFUNC_CACHE[fstart] = ida_hexrays.decompile(fstart)
        except Exception as e:
            msg("[hexrays] decompile failed at 0x%x: %s" % (fstart, e))
            CFUNC_CACHE[fstart] = None
    return CFUNC_CACHE.get(fstart)


def set_pseudocode_comment(ea, text):
    """Add a Hex-Rays user comment so it appears in pseudocode/F5 view."""
    cfunc = get_cfunc_for_ea(ea)
    if cfunc is None:
        return False
    try:
        itp = getattr(ida_hexrays, PSEUDOCODE_ITP_NAME)
    except Exception:
        itp = getattr(ida_hexrays, "ITP_SEMI", 0)

    try:
        tl = ida_hexrays.treeloc_t()
        tl.ea = ea
        tl.itp = itp
        try:
            old = cfunc.get_user_cmt(tl) or ""
        except Exception:
            old = ""
        if text in old:
            return True
        cmt = text if not old else old + "\n" + text
        cfunc.set_user_cmt(tl, cmt)
        cfunc.save_user_cmts()
        return True
    except Exception as e:
        msg("[hexrays] set pseudocode comment failed at 0x%x: %s" % (ea, e))
        return False


def set_comment(ea, text, pseudocode=False):
    old = idc.get_cmt(ea, 0) or ""
    if text not in old:
        cmt = text if not old else old + "\n" + text
        idc.set_cmt(ea, cmt, 0)
    if pseudocode:
        set_pseudocode_comment(ea, text)


def get_decode_ea():
    for name in ("decode", "sub_401B0"):
        ea = idc.get_name_ea_simple(name)
        if ea != ida_idaapi.BADADDR:
            return ea
    return DECODE_EA_FALLBACK


def norm_reg(op):
    op = (op or "").strip().upper()
    # remove common decoration if any
    op = op.split()[0] if op else op
    if op in ("XZR", "WZR", "ZR"):
        return "ZR"
    if op == "SP":
        return "SP"
    if len(op) >= 2 and op[0] in ("X", "W") and op[1:].isdigit():
        return "X" + op[1:]  # normalize W2/X2 together
    return op


def op_is_reg(ea, n, reg=None):
    r = norm_reg(idc.print_operand(ea, n))
    if reg is None:
        return r
    return r == reg


def is_branch_or_call(ea):
    m = idc.print_insn_mnem(ea).upper()
    # Stop local backward scan at prior call/branch. Keep ADR/ADRP safe.
    if m in ("BL", "BLR", "RET", "BR"):
        return True
    if m == "B" or m.startswith("B.") or m in ("CBZ", "CBNZ", "TBZ", "TBNZ"):
        return True
    return False


def writes_reg(ea, reg):
    m = idc.print_insn_mnem(ea).upper()
    if m in ("ADR", "ADRL", "ADRP", "ADD", "MOV", "MOVZ", "ORR", "LDR"):
        return op_is_reg(ea, 0, reg)
    return False


def prev_insns_until(ea, max_insns=MAX_BACK_INSNS):
    """Instructions before ea, after previous call/branch, returned forward order."""
    out = []
    func_start = idc.get_func_attr(ea, idc.FUNCATTR_START)
    if func_start == ida_idaapi.BADADDR:
        func_start = 0
    cur = idc.prev_head(ea, func_start)
    count = 0
    while cur != ida_idaapi.BADADDR and cur >= func_start and count < max_insns:
        if is_branch_or_call(cur):
            break
        out.append(cur)
        count += 1
        cur = idc.prev_head(cur, func_start)
    out.reverse()
    return out


def simulate(insns):
    regs = {}
    for ea in insns:
        m = idc.print_insn_mnem(ea).upper()
        dst = norm_reg(idc.print_operand(ea, 0))
        op1 = norm_reg(idc.print_operand(ea, 1))
        # Most IDA AArch64 operand values are already resolved EAs/immediates.
        v1 = idc.get_operand_value(ea, 1)
        v2 = idc.get_operand_value(ea, 2)
        t2 = idc.get_operand_type(ea, 2)

        if m in ("ADR", "ADRL"):
            regs[dst] = v1
        elif m == "ADRP":
            regs[dst] = v1
        elif m == "ADD":
            # ADD Xd, Xn, #imm
            if op1 in regs and t2 == idc.o_imm:
                regs[dst] = regs[op1] + v2
            else:
                regs.pop(dst, None)
        elif m in ("MOV", "MOVZ"):
            typ1 = idc.get_operand_type(ea, 1)
            if typ1 == idc.o_imm:
                regs[dst] = v1
            elif op1 in regs:
                regs[dst] = regs[op1]
            elif op1 == "ZR":
                regs[dst] = 0
            else:
                regs.pop(dst, None)
        elif m == "ORR":
            # IDA sometimes prints MOV alias, but support ORR Wd, WZR, #imm too.
            op2 = norm_reg(idc.print_operand(ea, 2))
            typ2 = idc.get_operand_type(ea, 2)
            if op1 == "ZR" and typ2 == idc.o_imm:
                regs[dst] = v2
            elif op1 in regs and op2 in regs:
                regs[dst] = regs[op1] | regs[op2]
            else:
                regs.pop(dst, None)
        elif writes_reg(ea, dst):
            regs.pop(dst, None)
    return regs


def resolve_direct(call_ea):
    regs = simulate(prev_insns_until(call_ea))
    blob = regs.get("X0")
    length = regs.get("X2")
    if blob is None or length is None:
        return None
    return int(blob), int(length), "direct"


def resolve_from_branch_source(branch_ea, forced_len=None):
    regs = simulate(prev_insns_until(branch_ea))
    blob = regs.get("X0")
    length = forced_len if forced_len is not None else regs.get("X2")
    if blob is None or length is None:
        return None
    return int(blob), int(length), "pred@0x%x" % branch_ea


def branch_refs_to(ea):
    refs = []
    try:
        for frm in idautils.CodeRefsTo(ea, 0):
            if frm != idc.prev_head(ea):
                refs.append(frm)
    except Exception:
        pass
    return refs


def resolve_candidates(call_ea):
    """Return [(blob_ea, len, source), ...]. Handles normal calls and simple shared-call blocks."""
    cand = []

    d = resolve_direct(call_ea)
    if d:
        cand.append(d)

    # Some code shares one `BL decode` behind a jump-table. Try direct branches to call_ea.
    for br in branch_refs_to(call_ea):
        d = resolve_from_branch_source(br)
        if d:
            cand.append(d)

    # Also handle branches to the instruction just before call_ea when that instruction sets W2/X2,
    # e.g. loc: MOV W2,#0x10; BL decode, with many preds setting X0.
    prev = idc.prev_head(call_ea)
    forced_len = None
    if norm_reg(idc.print_operand(prev, 0)) == "X2":
        m = idc.print_insn_mnem(prev).upper()
        if m in ("MOV", "MOVZ") and idc.get_operand_type(prev, 1) == idc.o_imm:
            forced_len = idc.get_operand_value(prev, 1)
    if forced_len is not None:
        for br in branch_refs_to(prev):
            d = resolve_from_branch_source(br, forced_len=forced_len)
            if d:
                cand.append(d)

    # de-duplicate by (blob,len)
    seen = set()
    out = []
    for blob, ln, src in cand:
        if blob <= 0 or ln <= 0 or ln > 0x1000:
            continue
        k = (blob, ln)
        if k not in seen:
            seen.add(k)
            out.append((blob, ln, src))
    return out


def is_call_to_decode(ea, decode_ea):
    m = idc.print_insn_mnem(ea).upper()
    if m != "BL":
        return False
    return idc.get_operand_value(ea, 0) == decode_ea


def main():
    decode_ea = get_decode_ea()
    msg("[+] decode function EA = 0x%x" % decode_ea)
    msg("[+] Hex-Rays pseudocode comments: %s" % ("enabled" if init_hexrays_once() else "disabled/unavailable"))

    call_sites = sorted(set(frm for frm in idautils.CodeRefsTo(decode_ea, 0) if is_call_to_decode(frm, decode_ea)))
    msg("[+] found %d direct BL call sites" % len(call_sites))

    ok = 0
    unresolved = []
    for call_ea in call_sites:
        entries = []
        for blob_ea, ln, src in resolve_candidates(call_ea):
            try:
                dec = decode_401B0(blob_ea, ln)
                text = pretty_bytes(dec)
                entries.append((blob_ea, ln, src, text, dec.hex()))
            except Exception as e:
                entries.append((blob_ea, ln, src, "<decode failed: %s>" % e, ""))

        if not entries:
            unresolved.append(call_ea)
            set_comment(call_ea, "decode: <unresolved x0/len> ; run-time/shared setup maybe", pseudocode=True)
            continue

        ok += 1
        lines = []
        for blob_ea, ln, src, text, hx in entries:
            lines.append('decode[0x%x,%d] -> "%s" (%s)' % (blob_ea, ln, text, src))
            if COMMENT_BLOB and not text.startswith("<decode failed"):
                set_comment(blob_ea, 'sub_401B0 decoded[%d]: "%s"' % (ln, text))
        if COMMENT_CALL:
            set_comment(call_ea, "\n".join(lines), pseudocode=True)

        if COMMENT_SETUP_SOURCE:
            for blob_ea, ln, src, text, hx in entries:
                if src.startswith("pred@0x"):
                    try:
                        src_ea = int(src[len("pred@"):], 16)
                        set_comment(src_ea, 'decode setup -> \"%s\" ; blob=0x%x len=%d' % (text, blob_ea, ln), pseudocode=True)
                    except Exception:
                        pass

        for line in lines:
            msg("0x%x: %s" % (call_ea, line))

    msg("[+] commented %d/%d call sites" % (ok, len(call_sites)))
    if unresolved:
        msg("[!] unresolved call sites: " + ", ".join("0x%x" % x for x in unresolved))
    try:
        ida_kernwin.refresh_idaview_anyway()
    except Exception:
        pass
    msg("[+] done. If pseudocode was already open, press F5/refresh or reopen the pseudocode window.")


if __name__ == "__main__":
    main()
